    private void btnDoiMatKhauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiMatKhauActionPerformed
        // TODO add your handling code here:
        TaiKhoan_Dao tk_Dao = new TaiKhoan_Dao();
        if (txt_matKhauMoi.getText().length() < 8) {
            NotifyToast.showErrorToast("Mật khẩu phải dài hơn 8 kí tự");
            return;
        }
        if (!txt_matKhauMoi.getText().equals(txt_xacNhanMK.getText())) {
            NotifyToast.showErrorToast("Mật khẩu không trùng khớp");
            return;
        }
        if (!txt_matKhauHT.getText().equals(Application.getTK().getMatKhau())) {
            NotifyToast.showErrorToast("Mật khẩu hiện tại không đúng");
            return;
        }
        if (tk_Dao.updateMatKhau(Application.getTK().getTenTaiKhoan(), txt_matKhauMoi.getText())) {
            NotifyToast.showSuccessToast("Thay đổi mật khẩu thành công. Hãy đăng nhập lại");
            dispose();
            Application.logout();
        } else NotifyToast.showErrorToast("Đã có lỗi xảy ra");
    }//GEN-LAST:event_btnDoiMatKhauActionPerformed

