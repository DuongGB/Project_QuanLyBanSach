    private void btn_tiepTucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tiepTucActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_dsHDCho.getSelectedRow();
        if (selectedRow != -1) {
            String hoTen = (String) tbl_dsHDCho.getValueAt(selectedRow, 0);
            String SDT = (String) tbl_dsHDCho.getValueAt(selectedRow, 1);
            user = new Object[]{hoTen, SDT};
            dispose();
        } else
            NotifyToast.showErrorToast("Hãy chọn hóa đơn cần thanh toán");
    }//GEN-LAST:event_btn_tiepTucActionPerformed

